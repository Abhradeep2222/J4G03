
public class PrimitiveType {
	public static void main(String[] args) {
		byte b = (byte)210;
		byte x = -46;
		System.out.println(b);
		
		long i = 34564343445560L;
		float f = 234343.00F;
		
		// Implicit casting happens between the wrapper classes 
		// and there corresponding primitive data type
		int n = new Integer(10);
		Integer in = 10;
		double d = new Double(256.33);

	}
}

